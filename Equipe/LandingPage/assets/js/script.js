function changeI() {
    var inicio = document.getElementById("inicio");
    var quemSomos = document.getElementById("quemSomos");
    var valores = document.getElementById("valores");
    var servicos = document.getElementById("servicos");
    var equipe = document.getElementById("equipe");
    inicio.style.display = "grid"
    quemSomos.style.display = "none";
    valores.style.display = "none";
    servicos.style.display = "none";
    equipe.style.display = "none";
}

